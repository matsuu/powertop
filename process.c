#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>
#include <sys/types.h>
#include <dirent.h>

#include "powertop.h"

void suggest_process_death(char *process, struct line *lines, int linecount, char *comment)
{
	int i;
	int found = 0;
	if (suggestioncount > 0)
		return;

	for (i = 0; i < linecount; i++) {
		if (strstr(lines[i].string, process)) {
			found++;
			printf("%s\n", comment);
			break;
		}
	}
	fflush(stdout);
	suggestioncount += found;
}
